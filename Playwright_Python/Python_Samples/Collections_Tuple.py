#Tuple
# Allows Duplicate
# Any Type of data can be stored
# We Can not Modify the tuple item. We cannot add or remove
tp_value = (1,3,5,6,7)
print(tp_value)
tp_list=list(tp_value)# Can Convert tuple in to List
print(tp_value)
tp_list.pop()# Can perform list operations like add or remove
print(tp_list)
